# Topic: Operators in Python

a = 10
b = 5

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Floor Division:", a // b)
print("Exponent:", a ** b)

# Comparison Operators
print(a > b)
print(a < b)
print(a == b)

# Logical Operators
print(a > 5 and b < 10)
print(a > 5 or b > 10)
print(not(a > b))
