# Topic: Data Types
# Part of my Python learning journey

# Integer
a = 10
print(type(a))

# Float
b = 5.5
print(type(b))

# String
c = "Hello"
print(type(c))

# Boolean
d = True
print(type(d))

# List
e = [1, 2, 3, 4]
print(type(e))

# Tuple
f = (1, 2, 3)
print(type(f))

# Set
g = {1, 2, 3}
print(type(g))

# Dictionary
h = {"name": "Uday", "age": 22}
print(type(h))
